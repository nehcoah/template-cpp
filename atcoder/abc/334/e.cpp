template<int V, int P>
constexpr MInt<P> CInv = MInt<P>(V).inv();

constexpr int P = 998244353;
using mint = MInt<P>;

int dirs[4][2] = {{0, -1}, {0, 1}, {-1, 0}, {1, 0}};

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int h, w;
    cin >> h >> w;
    vector<string> s(h);
    for (int i = 0; i < h; i++) {
        cin >> s[i];
    }
    vector<vector<int>> t(h, vector<int>(w));
    vector<vector<int>> vis(h, vector<int>(w));

    auto check = [&](int i, int j, int z) {
        queue<pair<int, int>> q;
        q.emplace(i, j);
        vis[i][j] = 1;
        while (!q.empty()) {
            auto [x, y] = q.front(); q.pop();
            t[x][y] = z;
            for (auto d : dirs) {
                int nx = x + d[0], ny = y + d[1];
                if (nx >= 0 && nx < h && ny >= 0 && ny < w && !vis[nx][ny] && s[nx][ny] == '#') {
                    q.emplace(nx, ny);
                    vis[nx][ny] = 1;
                }
            }
        }
    };

    int f = 0;
    for (int i = 0; i < h; i++) {
        for (int j = 0; j < w; j++) {
            if (s[i][j] == '#' && !vis[i][j]) {
                check(i, j, ++f);
            }
        }
    }

    mint ans = 0, cnt = 0;
    for (int i = 0; i < h; i++) {
        for (int j = 0; j < w; j++) {
            if (s[i][j] == '.') {
                cnt += 1;
                unordered_set<int> st;
                for (auto d : dirs) {
                    int x = i + d[0], y = j + d[1];
                    if (x >= 0 && x < h && y >= 0 && y < w && t[x][y]) st.insert(t[x][y]);
                }
                 ans += f - st.size() + 1;
            }
        }
    }
    cout << ans / cnt << endl;

    return 0;
}

