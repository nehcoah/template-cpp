using namespace std;
using ll = long long;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m;
    cin >> n >> m;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        m -= a[i];
        if (m < 0) {
            cout << i << endl;
            return 0;
        }
    }
    cout << n << endl;

    return 0;
}

