constexpr int P = 998244353;
using mint = MInt<P>;

struct Comb {
    int n;
    vector<mint> _fac;
    vector<mint> _invfac;
    vector<mint> _inv;

    Comb() : n{0}, _fac{1}, _invfac{1}, _inv{0} {}
    Comb(int n) : Comb() {
        init(n);
    }

    void init(int m) {
        m = min(m, mint::getMod() - 1);
        if (m <= n) return;
        _fac.resize(m + 1);
        _invfac.resize(m + 1);
        _inv.resize(m + 1);

        for (int i = n + 1; i <= m; i++) {
            _fac[i] = _fac[i - 1] * i;
        }
        _invfac[m] = _fac[m].inv();
        for (int i = m; i > n; i--) {
            _invfac[i - 1] = _invfac[i] * i;
            _inv[i] = _invfac[i] * _fac[i - 1];
        }
        n = m;
    }

    mint fac(int m) {
        if (m > n) init(2 * m);
        return _fac[m];
    }
    mint invfac(int m) {
        if (m > n) init(2 * m);
        return _invfac[m];
    }
    mint inv(int m) {
        if (m > n) init(2 * m);
        return _inv[m];
    }
    mint binom(int n, int m) {
        if (n < m || m < 0) return 0;
        return fac(n) * invfac(m) * invfac(n - m);
    }
} comb;


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int k;
    cin >> k;
    vector<int> c(26);
    for (int i = 0; i < 26; i++) {
        cin >> c[i];
    }
    comb.init(1e3);

    vector<mint> dp(k + 1);
    dp[0] = 1;
    for (int i = 0; i < 26; i++) {
        vector<mint> ndp(k + 1);
        for (int j = 0; j <= k; j++) {
            for (int d = 0; d <= c[i] && d + j <= k; d++) {
                ndp[j + d] += dp[j] * comb.binom(j + d, d);
            }
        }
        swap(dp, ndp);
    }
    mint ans = accumulate(dp.begin() + 1, dp.end(), mint(0));
    cout << ans << endl;

    return 0;
}

