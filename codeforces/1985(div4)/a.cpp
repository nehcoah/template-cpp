using namespace std;
using ll = long long;

void solve() {
    string s, t;
    cin >> s >> t;
    swap(s[0], t[0]);
    cout << s << " " << t << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}

